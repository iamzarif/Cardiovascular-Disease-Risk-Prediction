# Shared utilities for Parts 3-5: one canonical low->high risk-level mapping, a decoder for
# Part 1's alphabetically label-encoded target, and a preprocessing-pipeline factory. Defined
# once here so Parts 3-5 reuse the same objects instead of each redefining identical logic
# (previously: `ordinalCVDRisk` in Part 3, `target_map`/`decode_risk_level` in Part 4, and
