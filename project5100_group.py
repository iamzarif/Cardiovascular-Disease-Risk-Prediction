def decode_risk_level(df):
    # Part 1 label-encoded CVD Risk Level alphabetically; recover the original strings so
    # Parts 3-5 can apply the same low-to-high ordinal mapping (target_map) consistently.
    return pd.Series(
        label_encoders["CVD Risk Level"].inverse_transform(df["CVD Risk Level"]),
        index=df.index,
    )

def make_preprocessor(numeric_cols):
    # Numeric-only: Part 1/2 already label-encoded all categoricals and scaled numerics, so
    # every remaining column is already numeric - only median imputation is needed here.
    return ColumnTransformer([
        ("num", Pipeline([("imputer", SimpleImputer(strategy="median"))]), numeric_cols),
    ])

