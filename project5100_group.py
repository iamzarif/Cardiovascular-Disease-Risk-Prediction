def add_derived_features(df):
    """BMI Category, Pulse Pressure, MAP, Age Group - pure row-wise transforms, no fitting,
    so they can be applied to train and test independently with no leakage risk. Must run
    BEFORE scaling: the thresholds below are natural-unit clinical cutoffs (BMI 18.5/25/30,
    age 30/45/60), which only make sense on raw values, not standardized ones."""
    def bmi_category(bmi):
        if bmi < 18.5:
            return 0  # Underweight
        elif bmi < 25:
            return 1  # Normal
        elif bmi < 30:
            return 2  # Overweight
        else:
            return 3  # Obese

    def age_group(age):
        if age < 30:
            return 0
        elif age < 45:
            return 1
        elif age < 60:
            return 2
        else:
            return 3

    df = df.copy()
    df["BMI Category"] = df["BMI"].apply(bmi_category)
    df["Pulse Pressure"] = df["Systolic BP"] - df["Diastolic BP"]
    df["MAP"] = (df["Systolic BP"] + 2 * df["Diastolic BP"]) / 3
    df["Age Group"] = df["Age"].apply(age_group)
    return df


